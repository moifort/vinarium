import Foundation

// Real consumer wine coolers sold on Boulanger.fr / Darty.fr (and confirmed on
// manufacturer sites), with their manufacturer bottle capacity and number of
// temperature zones. Selecting one pre-fills the cellar grid via
// `CellarPreset.defaultGrid()`. Capacities with no reliable figure were dropped;
// zones that could not be confirmed default to 1. Sources: boutique.climadiff.com,
// lasommeliere.com, haier-europe.com, darty.com, boulanger.com, and the model
// spec sheets. Values are a starting point the user can adjust.
private func preset(_ brand: String, _ model: String, _ bottles: Int, _ zones: Int) -> CellarPreset {
    let slug = "\(brand) \(model)"
        .lowercased()
        .replacingOccurrences(of: "[^a-z0-9]+", with: "-", options: .regularExpression)
        .trimmingCharacters(in: CharacterSet(charactersIn: "-"))
    return CellarPreset(id: slug, brand: brand, model: model, bottles: bottles, zones: zones)
}

